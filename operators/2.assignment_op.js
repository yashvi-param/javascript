// Operator
// =	Assignment
// +=	Add and assign
// -=	Subtract and assign
// *=	Multiply and assign
// /=	Divide and assign
// %=	Modulus and assign
// **=	Exponent and assign

let a = 10;

console.log((a += 5)); // a=10+5 =15

console.log((a -= 5)); // a=15-5 = 10

console.log((a *= 5)); // a=10*5 = 50

console.log((a **= 5));