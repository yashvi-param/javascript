// Operator
// ==	Equal (value only)
// ===	Strict equal
// !=	Not equal (value only)
// !==	Strict not equal (value + type)
// >	Greater than	5 > 3
// <	Less than	5 < 3
// >=	Greater than or equal
// <=	Less than or equal

let a = 5;

let b = "5";

// check value  only ==
console.log(a == b);

// check value and datatypes ===

console.log(a === b);

let z = true;

console.log(z != 0);

// greater than

let c = 10;

console.log(c > a);

//  less than

console.log(c < a);

// >=

console.log(c >= a);

// <=

console.log(c <= a);